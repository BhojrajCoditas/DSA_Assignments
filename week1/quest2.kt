/*2. Write a Kotlin program that checks if a given number is even or odd.
   Input: 2
   Output: 2 is even
*/

fun main() {
    val inputNum: Int = 2
    if(inputNum%2 == 0){
        println("$inputNum is even.")
    } else {
        println("$inputNum is odd.")
    }
}

